package com.lazyan.antechou.pojo;

public class Ttype {
    private int id;
    private String type;
    private int uid;
    private int ieid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getIeid() {
        return ieid;
    }

    public void setIeid(int ieid) {
        this.ieid = ieid;
    }
}
